package com.Manbir;

public class Main {

    public static void main(String[] args) {

        int switchValue = 1;

        switch (switchValue) {
            case 1:
                System.out.println("Value is 1");
                break;

            case 2:
                System.out.println("Value is 2");
                break;

            case 3:
                System.out.println("Value is 3");
                break;

            default:
                System.out.println("none of the values");
                break;
        }

    }
}
